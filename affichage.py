import os

class Caractere:
    nb_line: int
    nb_column: int
    carac: list

    def __init__(self, _nb_line, _nb_column, _char):
        self.nb_column = _nb_column
        self.nb_line = _nb_line
        self.carac = _char

    def __str__(self):
        caractere = "\n"
        for line in range(self.nb_line):
            for column in range(self.nb_column):
                caractere += self.carac[line][column]
            caractere += "\n"
        return caractere

    def __add__(self,a):
        if self.nb_line != a.nb_line:
            raise ValueError
        else:
            list_char = [["" for k in range(self.nb_column + a.nb_column + 2)] for j in range(self.nb_line)]
            for j in range(self.nb_line):
                for k in range(self.nb_column):
                    list_char[j][k] = self.carac[j][k]
                for k in range(2):
                    list_char[j][self.nb_column + k] = "  "
                for k in range(a.nb_column):
                    list_char[j][self.nb_column + 2 + k] = a.carac[j][k]
            new_char = Caractere(self.nb_line, self.nb_column + 2 + a.nb_column, list_char)
            return new_char


